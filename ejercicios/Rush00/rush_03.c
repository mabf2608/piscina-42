#include <unistd.h>

void	rush(int x, int y)
{
	int	h;
	int	l;
	
	h = 1;
	l = 1;

	while (h <= y)
	{
		l = 1;
		while ( l <= x || l <= y)
		{
			if (l == 1 && l == y) 
			{
				write (1, "A", 1);
			}
			else if( (l > 1 && l < x) || (h > 1 && l < y))
			{
				write (1, "B", 1);
			}
			else
			{
				write (1, "C", 1);
			}
			l++;
		}
			write (1, "\n", 1);
		h++;
	}
}

int	main (void)
{
	rush (5, 5);
	return (0);
}
