#include <unistd.h>

void	ft_putchar(int i, int j, int y, int x)
{
	if ((i == 1 && (j == 1 || j == x)) || (i == y && (j == 1 || j == x)))
	{
		write (1, "o", 1);
	}
	else if (i > 1 && i < y && (j == 1 || j == x))
	{
		write (1, "|", 1);
	}
	else if (i > 1 && i < y && j > 1 && j < x)
	{
		write (1, " ", 1);
	}
	else
	{
		write (1, "-", 1);
	}
}
